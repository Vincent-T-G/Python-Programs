#This program is written while learning the if-elif-else construct
#The program checks if the value x is equal to, less than or greater than 5

#variable x
x = 5

#if-elif-else construct to check

if x == 5:
    print("x is equal to 5")
elif x < 5:
    print("x is less than 5")
else:
    print("x is greater than 5")
print("End of section")