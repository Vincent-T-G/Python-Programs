#This code is written in the process of learning the for loop

#Code to print numbers 0-4 using a for loop with range (stop)

for i in range(5):
    print(i)
print("Done using stop!")

#Code to print numbers 1-5 using a for loop with range (start, stop)

for i in range(1,6):
    print(i)
print("Done using start, stop!")

#Code to print numbers all odd numbers between 1 and 10 using a for loop with range (start, stop, step)

for i in range(1,10,2):
    print(i)
print("Done using start, stop, step!")